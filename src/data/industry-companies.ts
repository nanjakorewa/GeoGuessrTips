/** ticker配列から重複排除済みの企業リストを取得 */
export function getCompanies(tickers: string[]): Company[] {
  const seen = new Set<string>();
  return tickers
    .filter((t) => {
      if (seen.has(t)) return false;
      seen.add(t);
      return true;
    })
    .map((t) => companies[t])
    .filter(Boolean);
}
